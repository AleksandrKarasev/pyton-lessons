"""
В фермерском хозяйстве в Карелии выращивают чернику. 
Она растет на круглой грядке, причем кусты высажены 
только по окружности. 
Таким образом, у каждого куста есть ровно два соседних. 
Всего на грядке растет N кустов.
Эти кусты обладают разной урожайностью, 
поэтому ко времени сбора на них
выросло различное число ягод - на і-ом кусте вросло а. ягод.
В этом фермерском хозяйстве внедрена система 
автоматического сбора черники.
Эта система состоит из управляющего модуля 
и нескольких собирающих модулей.
Собирающий модуль за один заход, 
находясь непосредственно перед некоторым
кустом, собирает го стого куста ис двусосени с ним.
Напишите программу для нахождения максимального числа ягод, 
которое может собрать за один заход собирающий модуль, 
находясь перед некоторым кустом заданной 
во входном файле грядки.
"""

# Чтение данных
n = int(input())
berries = list(map(int, input().split()))

# Поиск максимальной суммы для тройки соседних кустов (с учётом кольца)
max_harvest = 0
for i in range(n):
    # Индексы соседей: i-1 и i+1 с циклическим переходом
    left = (i - 1) % n
    right = (i + 1) % n
    current_sum = berries[i] + berries[left] + berries[right]
    if current_sum > max_harvest:
        max_harvest = current_sum

# Вывод результата
print(max_harvest)