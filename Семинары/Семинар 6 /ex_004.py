"""
Два различных натуральных числа п и т называются дружественными, 
если сумма делителей числа п (включая 1, но исключая само п) 
равна числу т и наоборот. Например, 220 и 284 - дружественные числа.
По данному числу к выведите все пары дружественных чисел, 
каждое из которых не превосходит к. 
Программа получает на вход одно натуральное число к,
 не превосходящее 105. 
 Программа должна вывести все пары дружественных чисел, 
 каждое из которых не превосходит к. Пары необходимо выводить 
 по одной в строке, разделяя пробелами. Каждая пара должна 
 быть выведена только один раз (перестановка чисел новую пару не дает).
"""

k = int(input())

# Массив для хранения суммы собственных делителей
sum_div = [0] * (k + 1)

# Решето: для каждого i добавляем i ко всем кратным числам, начиная с 2*i
for i in range(1, k + 1):
    for j in range(2 * i, k + 1, i):
        sum_div[j] += i

# Поиск дружественных пар
for n in range(1, k + 1):
    m = sum_div[n]
    # Проверяем, что m больше n (чтобы не дублировать) и не превышает k,
    # и что сумма делителей m равна n
    if m > n and m <= k and sum_div[m] == n:
        print(n, m)