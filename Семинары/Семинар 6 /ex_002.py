"""
Дан массив, состоящий из целых чисел. Напишите программу, 
которая в данном массиве определить количество элементов, 
у которых два соседних и, при этом, оба соседних элемента меньше данного. 
Сначала вводится число N - количество элементов в массиве
Далее записаны N чисел — элементы массива. Массив состоит из целых чисел.
"""

N = int(input("Введите кол-во элементов в массиве "))
list_1 = []
for i in range(N):
    list_1.append(int(input(f"Введите {i+1} элемент массива ")))

print(list_1)

count = 0
# Проверяем элементы со 2-го по предпоследний (индексы от 1 до N-2)
for i in range(1, N - 1):
    if list_1[i] > list_1[i-1] and list_1[i] > list_1[i+1]:
        count += 1

print(count)