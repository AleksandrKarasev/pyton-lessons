"""
Дан список чисел. Посчитайте, сколько в нем пар элементов, равных друг другу. 
Считается, что любые два элемента, равные друг другу образуют одну пару, 
которую необходимо посчитать. Вводится список чисел. 
Все числа списка находятся на разных строках.
"""

N=int(input("Введите кол-во элементов в массиве N "))

list_1=list()

for i in range(N):
    list_1.append(int(input(f"Введите {i+1} элемента в массиве")))


# Подсчёт частоты каждого числа
freq = {}
for num in list_1:
    freq[num] = freq.get(num, 0) + 1

# Суммируем количество пар
total_pairs = 0
for count in freq.values():
    total_pairs += count * (count - 1) // 2

print(total_pairs)