"""
Задача 26: Напишите программу, которая на вход принимает два числа А и В, 
и возводит число А в целую степень В с помощью рекурсии.
"""

def power(a: float, b: int) -> float:
    """
    Рекурсивно возводит число a в целую степень b.
    Поддерживает отрицательные показатели степени.
    """
    if b == 0:
        return 1
    elif b > 0:
        return a * power(a, b - 1)
    else:  # b < 0
        return 1 / power(a, -b)
    


a = float(input("Введите основание A: "))
b = int(input("Введите показатель степени B (целое число): "))
result = power(a, b)
print(f"{a} в степени {b} = {result}")
