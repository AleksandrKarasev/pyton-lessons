"""
Напишите рекурсивную функцию sum(a, b), 
возвращающую сумму двух целых неотрицательных чисел. 
Из всех арифметических операций допускаются только +1 и -1.
Также нельзя использовать циклы.
"""

def sum(a: int, b: int) -> int:
    """
    Возвращает сумму a и b, используя только +1 и -1.
    Предполагается, что a и b — целые неотрицательные числа.
    """
    if b == 0:
        return a
    # Уменьшаем b на 1, увеличиваем a на 1 — рекурсивно приближаемся к базовому случаю
    return sum(a + 1, b - 1)

# Пример использования

a = int(input("Введите первое неотрицательное число A: "))
b = int(input("Введите второе неотрицательное число B: "))
result = sum(a, b)
print(f"Сумма {a} + {b} = {result}")