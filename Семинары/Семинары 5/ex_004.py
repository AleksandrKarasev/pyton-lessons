"""
Дано натуральное число N и последовательность из N элементов.
Требуется вывести эту последовательность в обратном порядке.
Примечание. В программе запрещается объявлять массивы 
и использовать циклы (даже для ввода и вывода).
"""

def reverse_input(n):
    """Рекурсивно читает n чисел и выводит их в обратном порядке."""
    if n == 0:
        return
    x = int(input())          # читаем текущее число
    reverse_input(n - 1)      # рекурсивно читаем и выводим остальные
    print(x, end=' ' if n > 1 else '')  # выводим после возврата из рекурсии

n = int(input())              # количество элементов
reverse_input(n)

